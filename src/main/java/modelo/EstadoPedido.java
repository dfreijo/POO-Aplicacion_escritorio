package modelo;

/**
 * El enum EstadoPedido define los estados posibles de un pedido en una tienda en línea.
 */
public enum EstadoPedido {
    /**
     * Estado "Enviado" indica que el pedido ha sido enviado al cliente.
     */
    ENVIADO,

    /**
     * Estado "Pendiente" indica que el pedido está pendiente de procesar o envío.
     */
    PENDIENTE
}